#7. Realiza un programa que lea 2 números por teclado y determine los siguientes aspectos:
#● Si los dos números son iguales
#● Si los dos números son diferentes
#● Si el primero es mayor que el segundo
#● Si el segundo es mayor o igual que el primero

nom1=int(input("Ingrese número 1: "))
nom2=int(input("Ingrese número 2: "))
if nom1 == nom2:
    print("Los dos números son iguales")
else:
    print("los dos números son diferentes")
if nom1 > nom2:
        print("El primero es mayor que el segundo")
else:
        print("El segundo es mayor o igual que el primero") 

